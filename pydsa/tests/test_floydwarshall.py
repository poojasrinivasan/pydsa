from pydsa.floydwarshall import floydwarshall


def test_floydwarshall():
    graph = {0: {1: 6, 2: 8},
             1: {4: 11},
             2: {3: 9},
             3: {},
             4: {5: 3},
             5: {2: 7, 3: 4}}
    assert floydwarshall(graph) == {0: {0: 0, 1: 6, 2: 8, 3: 17, 4: 17, 5: 20},
                                    1: {0: 10000, 1: 0, 2: 21, 3: 18, 4: 11, 5: 14},
                                    2: {0: 10000, 1: 10000, 2: 0, 3: 9, 4: 10000, 5: 10000},
                                    3: {0: 10000, 1: 10000, 2: 10000, 3: 0, 4: 10000, 5: 10000},
                                    4: {0: 10000, 1: 10000, 2: 10, 3: 7, 4: 0, 5: 3},
                                    5: {0: 10000, 1: 10000, 2: 7, 3: 4, 4: 10000, 5: 0}}
